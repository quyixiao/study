package javax.swing;
/**
 * Swing开发图形界面比AWT更加优秀：Swing轻量级组件，100%Java实现，不依赖于本地平台的图形界面，支持跨平台。
 * 
 * SWT：需调用底层平台的GUI实现（故AWT只能使用各种平台GUI组件的交集）。
 * 
 * Swing：提供比AWT更多的图形界面组件，几乎所有组件纯Java实现，无需考虑底层是否支持（故可提供JTabbedpane等容器和JTable等GUI组件）
 * 
 * Swing组件：采用MVC（Model-View-Controller模型-视图-控制器）设计模式，允许自定义Render改变GUI的外观。
 */
